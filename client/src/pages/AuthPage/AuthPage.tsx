import { useState, useEffect } from 'react'
import { useNavigate } from 'react-router-dom'
import { useAuthContext } from '@/contexts/AuthContext'
import { useSocketContext } from '@/contexts/SocketContext'
import { useAuthFlow } from '@/hooks/useAuthFlow'
import { Button } from '@/components/atoms/Button'
import { PinInput } from '@/components/atoms/PinInput'
import { Typography } from '@/components/atoms/Typography'
import logoBig from '@/assets/logo-big.png'
import { Routes } from '@/routes'

export type AuthMode = 'select' | 'owner-pin'

export function AuthPage() {
  const [pin, setPin] = useState('')
  const [mode, setMode] = useState<AuthMode>('select')
  const [randomOwnerPin, setRandomOwnerPin] = useState<string | null>(null)
  const navigate = useNavigate()
  const { login, setOwner } = useAuthContext()
  const { socket, connected } = useSocketContext()

  const { submitPin, error, loading, clearError } = useAuthFlow({
    socket,
    connected,
    setOwner,
    login,
  })

  // Fetch random owner PIN if generated by server (plug-and-play mode)
  useEffect(() => {
    fetch('/api/owner-pin')
      .then(res => res.json())
      .then(data => {
        if (data.isRandom && data.pin) {
          setRandomOwnerPin(data.pin)
        }
      })
      .catch(() => {
        // Silently fail — PIN entry still works manually
      })
  }, [])

  const handleOwnerClick = () => {
    setMode('owner-pin')
    setPin('')
    clearError()
  }

  const handleRefereeClick = () => {
    login('referee')
    navigate(Routes.DASHBOARD_REFEREE)
  }

  const handleSpectatorClick = () => {
    login('viewer')
    navigate(Routes.DASHBOARD_SPECTATOR)
  }

  const handlePinChange = (value: string) => {
    setPin(value)
    if (value.length >= 5 && value.length <= 8) {
      clearError()
    }
  }

  const handlePinSubmit = () => {
    submitPin(pin)
  }

  const handleBack = () => {
    setMode('select')
    setPin('')
    clearError()
  }

  return (
    <div className="flex flex-col items-center justify-center min-h-screen bg-surface gap-8 p-4">
      <div className="flex flex-col items-center gap-4">
        <img src={logoBig} alt="RallyOS" className="w-32 h-auto mb-2" />
        <Typography variant="title">
          {mode === 'select' ? 'Elige tu rol' : 'Ingresa tu PIN de Organizador'}
        </Typography>
      </div>

      {mode === 'select' ? (
        // Selection Mode - 3 buttons
        <div className="flex flex-col gap-4 w-full max-w-sm">
          <Button
            variant="primary"
            size="lg"
            onClick={handleOwnerClick}
            disabled={loading}
            animate={false}
          >
            Organizador
          </Button>
          <Button
            variant="secondary"
            size="lg"
            onClick={handleRefereeClick}
            disabled={loading}
            animate={false}
          >
            Árbitro
          </Button>
          <Button
            variant="outline"
            size="lg"
            onClick={handleSpectatorClick}
            disabled={loading}
            animate={false}
          >
            Espectador
          </Button>
        </div>
      ) : (
        // Owner PIN Mode
        <div className="flex flex-col gap-4 w-full max-w-sm">
          {randomOwnerPin ? (
            <>
              <Typography variant="body" className="text-center text-muted-foreground">
                PIN de organizador de este torneo
              </Typography>
              <div className="flex flex-col items-center gap-2 p-4 bg-surface-low rounded-lg border border-outline/20">
                <Typography variant="label" className="text-muted-foreground">
                  Tu PIN es:
                </Typography>
                <Typography variant="headline" className="font-mono text-primary tracking-widest">
                  {randomOwnerPin}
                </Typography>
                <Typography variant="label" className="text-xs text-muted-foreground">
                  Usalo para entrar como organizador
                </Typography>
              </div>
            </>
          ) : (
            <Typography variant="body" className="text-center text-muted-foreground">
              Ingresa el PIN de organizador del torneo
            </Typography>
          )}
          <PinInput
            length={8}
            value={pin}
            onChange={handlePinChange}
            onComplete={() => {}} // Auto-submit disabled - user must click button
            disabled={loading}
            error={error}
            autoFocus
            placeholder="••••••••"
          />

          {error && (
            <Typography variant="label" className="text-red-500 text-center">
              ⚠️ {error}
            </Typography>
          )}

          <Button
            className='bg-primary text-primary-foreground hover:bg-primary/90'
            variant="primary"
            disabled={pin.length < 5 || pin.length > 8 || loading}
            onClick={handlePinSubmit}
            animate={false}
          >
            {loading ? 'Verificando...' : 'Ingresar'}
          </Button>

          <Button
            variant="ghost"
            onClick={handleBack}
            disabled={loading}
            animate={false}
          >
            Atrás
          </Button>
        </div>
      )}
    </div>
  )
}
